if (num_kind_use_precomputed_FOs > 0) then
   do i = 1, num_kind_use_precomputed_FOs
      ! Search for the matching string
      do j = 1, max_obs_specific
         if(use_precomputed_FOs_these_obs_types(i) == obs_type_info(j)%name) then
